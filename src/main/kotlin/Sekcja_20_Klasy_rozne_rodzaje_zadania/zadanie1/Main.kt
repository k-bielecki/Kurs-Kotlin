interface Alkohol

abstract class Piwo : Alkohol

abstract class Wino : Alkohol

abstract class Wodka : Alkohol

class DeboweMocne : Piwo()

class Zywiec : Piwo()

class Chianti : Wino()

class Prosecco : Wino()

class Zubrowka : Wodka()

class ZoladkowaGorzka : Wodka()